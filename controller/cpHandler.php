        $query->execute();
        $results = $query->fetchAll(PDO::FETCH_OBJ);
        if ($query->rowCount() > 0) {
            $con = "update tblstudents set Password=:newpassword where EmailId=:email";
            $chngpwd1 = $dbh->prepare($con);
